export type Locale = "fr" | "ar" | "en";

export function useTranslations(locale: string = "fr") {
  const currentLocale = (locale === "ar" || locale === "en" ? locale : "fr") as Locale;
  
  return function t(key: keyof typeof translations["fr"]): string {
    return translations[currentLocale][key] || translations["fr"][key] || String(key);
  };
}
